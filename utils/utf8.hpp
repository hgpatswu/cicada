  inline
  bool utf8_start(const char c)
  {
    return ((c & 0x80) == 0) || ((c & 0xc0) == 0xc0);
  }
  
  inline
  bool utf8_other(const char c)
  {
    return (c & 0xc0) == 0x80;
  }
  
